# Operadores Aritmeticos, son operadores que nos permite ejecutar cualquier calculo, son muy importantes para el area
# de analisis de datos y tambien para el desarrollo web para calcular algo.

print(10 + 10) # Operador de suma
print(10 - 5) # Operador de sustracción o restar
print(10 * 2) # Operador de multiplicación
print(10 / 2) # Operador de division

# Operador de modulo o residuo
print(10 % 2) # Division exacta el residuo seria 0 
print(10 % 3) # Division inexacta el residuo seria 1

print(10 // 3) # Quedarnos con el número entero de una division
print(2 ** 3) # Operador de exponenciación 

# Operacion compleja

print(2 ** 3 + 3 - 7 / 1 // 4)

"""
  LA PRIORIDAD DE OPERACIONES ( DE IZQUIERDA A DERECHA )
  P -> Parentesis
  E -> Exponentes
  M -> Multiplicación
  D -> Division
  A -> Adicion
  S -> Sustraccion
"""

print(2 ** 3)
print((7 / 1) // 4)
print(8 + 3 - 1)

# CUIDADO CON LA DIVISION

print(10 / 0) # -> ERROR NO ES POSIBLE DIVIDIR ENTRE 0

